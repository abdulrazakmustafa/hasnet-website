    popup.style.display = 'flex';
  }, delay);
})();

function closeSubscribePopup(){
  document.getElementById('subscribe-popup').style.display = 'none';
  sessionStorage.setItem('hasnet_subscribed', '1');
}

function submitSubscribePopup(){
  var email    = document.getElementById('popup-email').value.trim();
  var whatsapp = document.getElementById('popup-whatsapp').value.trim();
  var btn      = document.getElementById('popup-btn');

  if (!email && !whatsapp) {
    document.getElementById('popup-email').style.borderColor = '#e53e3e';
    return;
  }

  btn.textContent = 'Subscribing...';
  btn.disabled = true;

  var fd = new FormData();
  fd.append('email', email);
  fd.append('whatsapp', whatsapp);
  fd.append('source', 'popup');

  fetch('subscribe.php', { method: 'POST', body: fd })
    .then(function(r){ return r.json(); })
    .then(function(data){
      if (data.ok) {
        document.getElementById('popup-form-area').style.display = 'none';
        document.getElementById('popup-success').style.display = 'block';
        sessionStorage.setItem('hasnet_subscribed', '1');
        setTimeout(closeSubscribePopup, 3000);
      } else {
        btn.textContent = 'Subscribe Now';
        btn.disabled = false;
        alert(data.message || 'Something went wrong.');
      }
    })
    .catch(function(){
      btn.textContent = 'Subscribe Now';
      btn.disabled = false;
    });
}
</script>
